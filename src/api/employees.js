/**
 * Fetches the list of employees.
 * @returns {Promise<Array>} A promise that resolves to an array of employee objects.
 */
export const fetchEmployees = () =>
  new Promise((res) => setTimeout(() => res([...EMPLOYEES]), 400));

/**
 * Fetches an employee by their ID.
 * @param id {string} The ID of the employee to fetch.
 * @returns {Promise<Object|null>} A promise that resolves to the employee object if found, or null if not found.
 */
export const fetchEmployeeById = (id) =>
  new Promise((res) => {
    setTimeout(() => {
      const employee = EMPLOYEES.find((e) => e.id === id);
      res(employee || null);
    }, 400);
  });

/**
 * Adds a new employee.
 * @param employee {Object} The employee object to add.
 * @returns {Promise<Object>} A promise that resolves to the newly added employee object.
 */
export const addEmployeeApi = (employee) =>
  new Promise((res) => {
    setTimeout(() => {
      const newEmp = {id: crypto.randomUUID(), ...employee};
      EMPLOYEES.push(newEmp);
      res(newEmp);
    }, 400);
  });

/**
 * Updates an existing employee.
 * @param id {string} The ID of the employee to update.
 * @param changes {Object} The changes to apply to the employee.
 * @returns {Promise<Object>} A promise that resolves to the updated employee object.
 */
export const updateEmployeeApi = (id, changes) =>
  new Promise((res) => {
    setTimeout(() => {
      EMPLOYEES = EMPLOYEES.map((e) => (e.id === id ? {...e, ...changes} : e));
      res(EMPLOYEES.find((e) => e.id === id));
    }, 400);
  });

/**
 * Deletes an employee by their ID.
 * @param id {string} The ID of the employee to delete.
 * @returns {Promise<string>} A promise that resolves to the ID of the deleted employee.
 */
export const deleteEmployeeApi = (id) =>
  new Promise((res) => {
    setTimeout(() => {
      EMPLOYEES = EMPLOYEES.filter((e) => e.id !== id);
      res(id);
    }, 400);
  });
