export const fetchEmployees = () =>
  new Promise((res) => setTimeout(() => res([...EMPLOYEES]), 400));

export const addEmployeeApi = (employee) =>
  new Promise((res) => {
    setTimeout(() => {
      const newEmp = {id: crypto.randomUUID(), ...employee};
      EMPLOYEES.push(newEmp);
      res(newEmp);
    }, 400);
  });

export const updateEmployeeApi = (id, changes) =>
  new Promise((res) => {
    setTimeout(() => {
      EMPLOYEES = EMPLOYEES.map((e) => (e.id === id ? {...e, ...changes} : e));
      res(EMPLOYEES.find((e) => e.id === id));
    }, 400);
  });

export const deleteEmployeeApi = (id) =>
  new Promise((res) => {
    setTimeout(() => {
      EMPLOYEES = EMPLOYEES.filter((e) => e.id !== id);
      res(id);
    }, 400);
  });
